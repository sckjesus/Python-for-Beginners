# Printing "Hello" plus " World"
print("Hello"+" World")
# Printing "Hello" //a//dded to " World"
print("Hello\a World")
# Printing "Hello" plus the //f//emale sign plus " World"
print("Hello\f World")
# Printing "Hello" plus the male sign plus " World"
print("Hello\v World")
# Printing "Hello"+ a tab +" World"
print("Hello\t World")
# Printing "Hello", and then on the next line, " World"
print("Hello\n World")